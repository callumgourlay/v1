// Allow local development override by setting NEXT_PUBLIC_DEV_DOMAIN to one of the keys below.
const devOverride = process.env.NEXT_PUBLIC_DEV_DOMAIN
  ? process.env.NEXT_PUBLIC_DEV_DOMAIN.toLowerCase()
  : null;

export function getDomainConfig(hostname = "scotsmart.co.uk") {
  const cleaned = hostname.replace(/^https?:\/\//, "").split(":")[0].toLowerCase();

  const isLocalHost = cleaned === "localhost" || cleaned === "127.0.0.1";
  if (isLocalHost && devOverride && domains[devOverride]) {
    return domains[devOverride];
  }

  if (domains[cleaned]) return domains[cleaned];
  if (devOverride && domains[devOverride]) return domains[devOverride];
  return domains["scotsmart.co.uk"];
}

